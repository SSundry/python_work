print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)

#Use ** for exponents, so 3**2 = 3^2
print(3 ** 2)
#Supports order of operations too
print ((2 + 3) * 4)

#A float is any number with a decimal point
print(0.1 + 0.1)
#When you divide two numbers(even if they are intergers) the result will be a float
print (4/2)
#Python defaults to a float in any operation that uses a float, even if the output is a whole number
print (1 + 2.0)
print (2 * 3.0)
print (3.0 ** 2)

#You can use underscores to make large numbers more readable
universe_age = 14_000_000_000
print(universe_age)
